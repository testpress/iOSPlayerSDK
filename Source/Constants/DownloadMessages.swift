import Foundation

public struct DownloadMessages {
    public static let started = NSLocalizedString("Download Started", comment: "Message shown when download starts")
    public static let paused = NSLocalizedString("Download Paused", comment: "Message shown when download is paused")
    public static let resumed = NSLocalizedString("Download Resumed", comment: "Message shown when download is resumed")
    public static let completed = NSLocalizedString("Download Completed", comment: "Message shown when download completes successfully")
    public static let failed = NSLocalizedString("Download Failed", comment: "Message shown when download fails")
}
